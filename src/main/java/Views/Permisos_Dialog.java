    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        ArrayList<Permiso> arrPermisos = new ArrayList<>();

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            Permiso p = new Permiso();

            String nombre = jTable1.getValueAt(i, jTable1.getColumn("Nombre").getModelIndex()).toString();
            boolean admini = (boolean) jTable1.getValueAt(i, jTable1.getColumn("Administracion").getModelIndex());
            boolean coti = (boolean) jTable1.getValueAt(i, jTable1.getColumn("Cotizacion").getModelIndex());
            boolean guias = (boolean) jTable1.getValueAt(i, jTable1.getColumn("Guias").getModelIndex());
            boolean despacho = (boolean) jTable1.getValueAt(i, jTable1.getColumn("Despacho").getModelIndex());
            boolean factura = (boolean) jTable1.getValueAt(i, jTable1.getColumn("Factura").getModelIndex());

            p.setNombre(nombre);
            p.setAdmini(admini);
            p.setCoti(coti);
            p.setGuias(guias);
            p.setDespacho(despacho);
            p.setFactura(factura);
            arrPermisos.add(p);
        }
        if (CRUD_Permisos.actualizarPermisos(arrPermisos)) {

            JOptionPane.showConfirmDialog(Main.frame, "ยก Permisos actualizados correctamente !", "LOGRADO", JOptionPane.DEFAULT_OPTION, 1, Functions.imagenCheck());
        } else {
            JOptionPane.showMessageDialog(Main.frame, "No se pudo actualizar ningun permiso", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed


