    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here: GUARDAR

        String guia = jComboBox2.getSelectedItem().toString();
        String patente = jComboBox3.getSelectedItem().toString();
        String nombre = jComboBox6.getSelectedItem().toString();
        if (!guia.isEmpty() && !patente.isEmpty() && !nombre.isEmpty()) {

            if (CRUD_Transporte.validarPatenteConductor(patente, nombre)) {
                int id = CRUD_Transporte.getIdConductor(nombre);

                if (CRUD_Transporte.validarPatente(patente)) {
                    if (CRUD_Transporte.updateIdConductor(patente, id)) {
                        Procedures.setAsignarConductorAGuia(jTable1, guia, patente, nombre);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ups algo salio mal ", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                } else { // insertar patente con la id
                    if (CRUD_Transporte.ingresarVehiculoXID(patente, id)) {
                        Procedures.setAsignarConductorAGuia(jTable1, guia, patente, nombre);
                    } else {
                        JOptionPane.showMessageDialog(this, "Ups algo salio mal ", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }

            } else {
                Procedures.setAsignarConductorAGuia(jTable1, guia, patente, nombre);
            }
        } else {
            JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

