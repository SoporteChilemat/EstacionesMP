    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String cliente = jTextField1.getText().toUpperCase();
        String rut = jTextField2.getText();

        if (!cliente.isEmpty() && !rut.isEmpty()) {
            if (Functions.validarRut(rut)) {
                rut = rut.replace(".", "").replace("-", "");
                if (CRUD_Clientes.ingresarCliente(cliente, rut)) {
                    Cliente c = new Cliente(cliente, rut);
                    JOptionPane.showConfirmDialog(this, "ยก Ingreso Exitoso !", "Guardar", JOptionPane.DEFAULT_OPTION, 1, Functions.imagenCheck());
                    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                    model.addRow(Functions.agregarCliente(c));
                    Views.Main.jComboBox4.addItem(c.getCliente());
                } else {
                    JOptionPane.showMessageDialog(this, "Ups! algo salio mal", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Rut invalido ", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField2.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

