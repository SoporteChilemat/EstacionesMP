    private void login() {
        try {
            String nombre = jTextField2.getText().trim();
            String contrasena = jPasswordField1.getText().trim();

            if (!nombre.equals("") && !contrasena.equals("")) {
                User user = CRUD_Users.getUser(nombre, contrasena);
                if (user != null) {

                    Main m = new Main(user);
                    m.setLocationRelativeTo(this);
                    m.setVisible(true);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(this, "UPS! OCURRIO UN PROBLEMA", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            System.out.println("Login login " + ex);
        }

    }

