    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String factura = jTextField5.getText();
        String cliente = jComboBox4.getSelectedItem().toString();
        String guia = jComboBox5.getSelectedItem().toString();
        DefaultTableModel model = (DefaultTableModel) jTable4.getModel();

        if (!factura.isEmpty() && !cliente.isEmpty() && !guia.isEmpty()) {
            ImageIcon ico = new ImageIcon(Procedures.dir + "\\Sources\\factura.png");
            Image image = ico.getImage();
            Image newimg = image.getScaledInstance(50, 50, java.awt.Image.SCALE_SMOOTH);
            ico = new ImageIcon(newimg);

            int resp = JOptionPane.showConfirmDialog(this, "¿Desea relacionar Factura N°" + factura + " con la Guia N°" + guia + "?,\npara el Cliente " + cliente, "ASIGNAR FACTURA", JOptionPane.YES_NO_OPTION, 1, ico);
            if (0 == resp) {
                int rut = CRUD_Clientes.getRutCliente(cliente);
                if (CRUD_Facturas.relacionarFact_Guia_Client(factura, rut, guia)) {
                    String oc = CRUD_Cotizaciones.getNumOC(guia);
                    Object[] agregar = Functions.agregarFactura(factura, oc, guia, cliente);
                    JOptionPane.showConfirmDialog(Main.frame, "¡ Relacion exitosa !", "LOGRADO", JOptionPane.OK_OPTION, 1, Functions.imagenCheck());
                    model.addRow(agregar);
                    jTable4.repaint();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

