    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String correo = jTextField1.getText().toUpperCase();
        String cliente = jComboBox1.getSelectedItem().toString();
        String rut = jComboBox2.getSelectedItem().toString().replace(".", "").replace("-", "");

        if (!correo.isEmpty() && !cliente.isEmpty() && !rut.isEmpty()) {
            if (CRUD_Clientes.validarCliente(cliente, rut)) {
                if (EmailValidator.getInstance().isValid(correo)) {
                    if (CRUD_Correos.ingresarCorreoCliente(correo, rut)) {
                        Correo c = new Correo(correo, cliente, rut, 0);
                        JOptionPane.showConfirmDialog(this, "ยก Ingreso Exitoso !", "Guardar", JOptionPane.DEFAULT_OPTION, 1, Functions.imagenCheck());
                        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                        model.addRow(Functions.agregarCorreo(c));
                    } else {
                        JOptionPane.showMessageDialog(this, "Ups algo salio mal ", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "NO ES UN CORREO VALIDO", "Error", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "VALIDAR RUT Y CLIENTE SI CORRESPONDEN ENTRE ELLOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "HAY CAMPOS VACIOS", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

