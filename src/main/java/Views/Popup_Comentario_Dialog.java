    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //GUARDAR INFORMACION
        String comentario = jTextArea1.getText().toUpperCase();
        int row = table.getSelectedRow();
        int col = table.getColumn("Comentario").getModelIndex();
        int colGuia = table.getColumn("Guia").getModelIndex();

        int guia = Integer.parseInt(table.getValueAt(row, colGuia).toString());

        if (CRUD_Guias.setComentario(comentario, guia)) {

            ImageIcon icon = new ImageIcon(Procedures.dir + "\\Sources\\check.png");
            Image image = icon.getImage();
            Image newimg = image.getScaledInstance(30, 30, java.awt.Image.SCALE_SMOOTH);
            icon = new ImageIcon(newimg);
            table.setValueAt(comentario, row, col);
            JOptionPane.showConfirmDialog(Views.Main.frame, "ยก Comentario Guardado !", "Comentario Exitoso", JOptionPane.DEFAULT_OPTION, 1, icon);
        } else {
            JOptionPane.showMessageDialog(Views.Main.frame, "Ups algo salio mal ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

