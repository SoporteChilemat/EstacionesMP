package Object;

/**
 *
 * @author Jason
 */
public class Cotizacion {

    int cotizacion;
    String fechaCierre;
    String fechaT;
    int guia;
    String numOc;
    String fechaOc;
    int valor;

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getCotizacion() {
        return cotizacion;
    }

    public void setCotizacion(int cotizacion) {
        this.cotizacion = cotizacion;
    }

    public String getFechaCierre() {
        return fechaCierre;
    }

    public void setFechaCierre(String fechaCierre) {
        this.fechaCierre = fechaCierre;
    }

    public String getFechaT() {
        return fechaT;
    }

    public void setFechaT(String fechaT) {
        this.fechaT = fechaT;
    }

    public int getGuia() {
        return guia;
    }

    public void setGuia(int guia) {
        this.guia = guia;
    }

    public String getNumOc() {
        return numOc;
    }

    public void setNumOc(String numOc) {
        this.numOc = numOc;
    }

    public String getFechaOc() {
        return fechaOc;
    }

    public void setFechaOc(String fechaOc) {
        this.fechaOc = fechaOc;
    }

}
